package com.society.bean;

public class ExpenditureDetailsBean 
{
	private Long pkSubExpId;
	private String expenditueName;
	private String subExpenditueName;
		
	
	public Long getPkSubExpId() {
		return pkSubExpId;
	}
	public void setPkSubExpId(Long pkSubExpId) {
		this.pkSubExpId = pkSubExpId;
	}
	public String getExpenditueName() {
		return expenditueName;
	}
	public void setExpenditueName(String expenditueName) {
		this.expenditueName = expenditueName;
	}
	public String getSubExpenditueName() {
		return subExpenditueName;
	}
	public void setSubExpenditueName(String subExpenditueName) {
		this.subExpenditueName = subExpenditueName;
	}
	
	
}
